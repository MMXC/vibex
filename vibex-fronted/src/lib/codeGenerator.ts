export interface GenerateResult {
  /** Generated files */
  files: GeneratedFiles;
  /** Total nodes in flow */
  nodeCount: number;
  /** Whether node limit was exceeded */
  limitExceeded: boolean;
  /** Limit that was exceeded */
  limit: number;
  /** Sanitized component name */
  componentName: string;
}

/**
 * Generate code files from a canvas flow.
 *
 * @param flow - The canvas flow with nodes
 * @param framework - Target framework (default: react)
 * @returns Generated files and metadata
 */
export function generateComponentCode(
  flow: CanvasFlow,
  framework: TargetFramework = 'react'
): GenerateResult {
  const nodes = flow.nodes ?? [];
  const limitExceeded = nodes.length > MAX_NODES_LIMIT;
  const flowName = sanitizeName(flow.name ?? 'Flow');

  return {
    files: {
      types: generateTypeDefinitions(flow),
      component: generateTSXSkeleton(flow, framework),
      css: generateCSSModule(flow),
      scss: generateSCSS(flow),
      js: generateJSConstants(flow),
      index: generateIndexFile(flow, framework),
    },
    nodeCount: nodes.length,
    limitExceeded,
    limit: MAX_NODES_LIMIT,
    componentName: `${flowName}Component`,
  };
}

/**
 * Package generated files as a ZIP blob.
 *
 * @param result - Result from generateComponentCode
 * @param flowName - Optional override for the file prefix (default: flow name)
 * @returns ZIP blob ready for download
 */
export async function packageAsZip(
  result: GenerateResult,
  flowName?: string
): Promise<Blob> {
  const prefix = sanitizeName(flowName ?? result.componentName.replace(/Component$/, ''));
  const zip = new JSZip();

  // Add all generated files
  zip.file(`${prefix}.types.ts`, result.files.types);
  zip.file(`${prefix}.tsx`, result.files.component);
  zip.file(`${prefix}.module.css`, result.files.css);
  zip.file(`${prefix}.scss`, result.files.scss);
  zip.file(`${prefix}.constants.ts`, result.files.js);
  zip.file(`index.ts`, result.files.index);

  // Add README
  zip.file('README.md', [
    `# ${prefix}`,
    '',
    'Auto-generated by VibeX Code Generator',
    '',
    '## Files',
    '',
    `- \`${prefix}.types.ts\` — TypeScript type definitions`,
    `- \`${prefix}.tsx\` — Component skeleton`,
    `- \`${prefix}.module.css\` — CSS Module (design system variables)`,
    `- \`${prefix}.scss\` — SCSS variables (E1)`,
    `- \`${prefix}.constants.ts\` — JS/TS constants (E1)`,
    `- \`index.ts\` — Entry point`,
    '',
    '## Usage',
    '',
    '```tsx',
    `import { ${result.componentName} } from './${prefix}';`,
    '',
    `<${result.componentName} initialData={{}} />`,
    '```',
    '',
    `Generated from ${result.nodeCount} canvas nodes.`,
    result.limitExceeded
      ? `\n⚠️ Warning: Node count (${result.nodeCount}) exceeded limit (${result.limit}).`
      : '',
  ].join('\n'));

  return zip.generateAsync({ type: 'blob', compression: 'DEFLATE' });
}

