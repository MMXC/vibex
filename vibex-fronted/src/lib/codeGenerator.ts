/** A canvas flow with its associated nodes */
export interface CanvasFlow {
  id?: string;
  name?: string;
  nodes?: CanvasNode[];
}

/** A canvas node (context, flow, or component) */
export interface CanvasNode {
  id: string;
  name?: string;
  type?: string;
  description?: string;
  position?: { x: number; y: number };
  [key: string]: unknown;
}

/** Node type enumeration for canvas nodes */
export type CanvasNodeType =
  | 'bounded-context'
  | 'flow'
  | 'component'
  | 'user-task'
  | 'system-task'
  | 'event'
  | 'gateway'
  | 'page'
  | 'form'
  | 'list'
  | 'detail'
  | 'modal';

/** A chapter/section in a flow */
export interface Chapter {
  id: string;
  name: string;
  steps?: Array<{
    id: string;
    name: string;
    type: string;
  }>;
}

/** Framework target for code generation */
export type TargetFramework = 'react' | 'vue' | 'solid';

/** Generated file bundle */
export interface GeneratedFiles {
  types: string;      // TypeScript type definitions (.d.ts)
  component: string;  // Component skeleton (.tsx)
  css: string;       // CSS Module (.module.css)
  scss: string;      // SCSS variables (.scss)
  js: string;        // JS/TS constants (.ts)
  index: string;     // Entry point (.ts)
}

