/**
 * Generate TypeScript type definitions from canvas nodes.
 * Outputs interfaces for Flow, CanvasNode, Chapter, and all node types.
 */
function generateTypeDefinitions(flow: CanvasFlow): string {
  const flowName = sanitizeName(flow.name ?? 'Flow');
  const componentName = `${flowName}Component`;
  const typesName = `${flowName}Types`;

  const nodes = flow.nodes ?? [];
  const contextNodes = nodes.filter((n) => n.type === 'bounded-context' || n.type === 'context');
  const flowNodes = nodes.filter((n) => n.type === 'flow' || n.type === 'user-task' || n.type === 'system-task');
  const componentNodes = nodes.filter((n) => n.type === 'component' || n.type === 'page' || n.type === 'form' || n.type === 'list');

  const lines: string[] = [
    `/**`,
    ` * ${typesName}`,
    ` * Auto-generated by VibeX Code Generator`,
    ` * Canvas Flow: ${flow.name ?? 'Unnamed'}`,
    ` */`,
    ``,
    `// ============================================================================`,
    `// Canvas Core Types`,
    `// ============================================================================`,
    ``,
    `export interface CanvasNode {`,
    `  id: string;`,
    `  name?: string;`,
    `  type?: string;`,
    `  description?: string;`,
    `  position?: { x: number; y: number };`,
    `  [key: string]: unknown;`,
    `}`,
    ``,
    `export interface Chapter {`,
    `  id: string;`,
    `  name: string;`,
    `  steps?: Array<{ id: string; name: string; type: string }>;`,
    `}`,
    ``,
    `export interface CanvasFlow {`,
    `  id?: string;`,
    `  name?: string;`,
    `  nodes?: CanvasNode[];`,
    `}`,
    ``,
    `// ============================================================================`,
    `// Flow-Specific Types — ${flowName}`,
    `// ============================================================================`,
    ``,
    `export type ${flowName}NodeType =`,
    contextNodes.length > 0 ? `  | 'bounded-context'` : '',
    flowNodes.length > 0 ? `  | 'flow' | 'user-task' | 'system-task'` : '',
    componentNodes.length > 0 ? `  | 'component' | 'page' | 'form' | 'list'` : '',
    `  | string;`,
    ``,
  ];

  // Generate per-node-type interfaces
  if (contextNodes.length > 0) {
    lines.push(`export interface ${flowName}ContextNode extends CanvasNode {`);
    lines.push(`  type: 'bounded-context';`);
    lines.push(`  children?: string[]; // IDs of child nodes`);
    lines.push(`}`);
    lines.push(``);
  }

  if (flowNodes.length > 0) {
    lines.push(`export interface ${flowName}FlowNode extends CanvasNode {`);
    lines.push(`  type: 'flow';`);
    lines.push(`  actor?: string;`);
    lines.push(`  triggers?: string[];`);
    lines.push(`  steps?: Array<{ id: string; name: string; type: string }>;`);
    lines.push(`}`);
    lines.push(``);
  }

  if (componentNodes.length > 0) {
    lines.push(`export interface ${flowName}ComponentNode extends CanvasNode {`);
    lines.push(`  type: 'component';`);
    lines.push(`  props?: Record<string, unknown>;`);
    lines.push(`  api?: { method: 'GET' | 'POST'; path: string };`);
    lines.push(`}`);
    lines.push(``);
  }

  // Node map type
  lines.push(`export type ${flowName}Node =`);
  if (contextNodes.length > 0) lines.push(`  | ${flowName}ContextNode`);
  if (flowNodes.length > 0) lines.push(`  | ${flowName}FlowNode`);
  if (componentNodes.length > 0) lines.push(`  | ${flowName}ComponentNode`);
  lines.push(`  | CanvasNode;`);
  lines.push(``);

  lines.push(`// ============================================================================`);
  lines.push(`// Component Props`);
  lines.push(`// ============================================================================`);
  lines.push(``);
  lines.push(`export interface ${componentName}Props {`);
  lines.push(`  /** Optional className override */`);
  lines.push(`  className?: string;`);
  lines.push(`  /** Initial data for the component */`);
  lines.push(`  initialData?: Partial<${flowName}Node>;`);
  lines.push(`  /** Callback when data changes */`);
  lines.push(`  onChange?: (data: Partial<${flowName}Node>) => void;`);
  lines.push(`}`);

  return lines.filter(Boolean).join('\n');
}

