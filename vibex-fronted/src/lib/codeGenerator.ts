/**
 * Generate a TSX component skeleton.
 * Uses CSS variables for all design tokens — no hardcoded values.
 */
function generateTSXSkeleton(flow: CanvasFlow, framework: TargetFramework): string {
  const flowName = sanitizeName(flow.name ?? 'Flow');
  const componentName = `${flowName}Component`;
  const nodes = flow.nodes ?? [];

  const imports: string[] = [];
  const jsxContent: string[] = [];

  if (framework === 'react') {
    imports.push(`import React from 'react';`);
    imports.push(`import type { ${componentName}Props } from './${flowName}.types';`);
    imports.push(`import styles from './${flowName}.module.css';`);
    imports.push(``);
    imports.push(`/**`);
    imports.push(` * ${componentName} — auto-generated TSX skeleton`);
    imports.push(` *`);
    imports.push(` * @generated by VibeX Code Generator`);
    imports.push(` * @TODO Implement component logic based on canvas design`);
    imports.push(` */`);
    imports.push(``);
    imports.push(`export function ${componentName}({ className, initialData, onChange }: ${componentName}Props) {`);

    // TODO comments for each node type
    const contextNodes = nodes.filter((n) => n.type === 'bounded-context');
    const flowNodes = nodes.filter((n) => n.type === 'flow' || n.type === 'user-task');
    const componentNodes = nodes.filter((n) => n.type === 'component');

    if (contextNodes.length > 0) {
      jsxContent.push(`  // TODO: Implement bounded context sections (${contextNodes.length} contexts)`);
      for (const node of contextNodes.slice(0, 3)) {
        jsxContent.push(`  //   - ${node.name ?? node.id} (${node.type})`);
      }
    }

    if (flowNodes.length > 0) {
      jsxContent.push(`  // TODO: Implement flow steps (${flowNodes.length} flows)`);
      for (const node of flowNodes.slice(0, 3)) {
        jsxContent.push(`  //   - ${node.name ?? node.id} (${node.type})`);
      }
    }

    if (componentNodes.length > 0) {
      jsxContent.push(`  // TODO: Implement component rendering (${componentNodes.length} components)`);
      for (const node of componentNodes.slice(0, 3)) {
        jsxContent.push(`  //   - ${node.name ?? node.id} (${node.type})`);
      }
    }

    jsxContent.push(``);
    jsxContent.push(`  return (`);
    jsxContent.push(`    <div className={\`\${styles.container}\${className ? \` \${className}\` : ''}\`}>`);
    jsxContent.push(`      {/* @TODO: Replace with actual component implementation */}`);
    jsxContent.push(`      <div className={styles.header}>`);
    jsxContent.push(`        <h2 className={styles.title}>{initialData?.name ?? '${flowName}'}</h2>`);
    jsxContent.push(`      </div>`);
    jsxContent.push(`      <div className={styles.content}>`);
    jsxContent.push(`        {/* @TODO: Add flow and component rendering */}`);
    jsxContent.push(`      </div>`);
    jsxContent.push(`    </div>`);
    jsxContent.push(`  );`);
    jsxContent.push(`}`);

  } else if (framework === 'vue') {
    imports.push(`<script setup lang="ts">`);
    imports.push(`import type { ${componentName}Props } from './${flowName}.types';`);
    imports.push(`defineProps<${componentName}Props>();`);
    imports.push(`</script>`);
    imports.push(``);
    imports.push(`<template>`);
    imports.push(`  <div class="${flowName.toLowerCase()}">`);
    imports.push(`    <!-- @TODO: Implement Vue component -->`);
    imports.push(`  </div>`);
    imports.push(`</template>`);

  } else {
    imports.push(`// @TODO: Implement ${framework} component`);
    imports.push(`export function ${componentName}() { return null; }`);
  }

  return [...imports, '', ...jsxContent].join('\n');
}

