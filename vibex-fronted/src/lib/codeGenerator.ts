/**
 * Generate the index.ts entry point.
 */
function generateIndexFile(flow: CanvasFlow, framework: TargetFramework): string {
  const flowName = sanitizeName(flow.name ?? 'Flow');
  const componentName = `${flowName}Component`;

  if (framework === 'react') {
    return [
      `/**`,
      ` * ${flowName} — auto-generated index`,
      ` * @generated by VibeX Code Generator`,
      ` */`,
      ``,
      `export type { ${flowName}Node, ${flowName}FlowNode, ${flowName}ContextNode, ${flowName}ComponentNode } from './${flowName}.types';`,
      `export type { ${componentName}Props } from './${flowName}.types';`,
      `export { ${componentName} } from './${flowName}.tsx';`,
    ].join('\n');
  }

  return `export { ${componentName} } from './${flowName}.${framework === 'vue' ? 'vue' : 'tsx'}';`;
}

